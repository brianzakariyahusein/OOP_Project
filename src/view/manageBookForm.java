    private void loadData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblBook.getModel();
        model.setRowCount(0); // Kosongkan tabel dulu

        dao.BookDAO bookDAO = new dao.BookDAO();
        java.util.List<model.BookModel> list = bookDAO.getAllBooks();

        for (model.BookModel book : list) {
            model.addRow(new Object[]{
                book.getBookId(),
                book.getTitle(),
                book.getAuthor(),
                book.getCategory(),
                book.getYearPublished(),
                book.getStock(),
                book.getCreatedAt()
            });
        }
    }

