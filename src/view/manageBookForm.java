    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int baris = tblBook.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih buku yang mau diedit!");
            return;
        }

        // Ambil data dari tabel untuk dilempar ke pop-up edit
        int id = Integer.parseInt(tblBook.getValueAt(baris, 0).toString());
        String title = tblBook.getValueAt(baris, 1).toString();
        String author = tblBook.getValueAt(baris, 2).toString();
        String category = tblBook.getValueAt(baris, 3).toString();
        int year = Integer.parseInt(tblBook.getValueAt(baris, 4).toString());
        int stock = Integer.parseInt(tblBook.getValueAt(baris, 5).toString());

        view.EditBookDialog editPopUp = new view.EditBookDialog(this, true, id, title, author, category, year, stock);
        editPopUp.setVisible(true);
        loadData();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int baris = tblBook.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih buku yang mau dihapus!");
            return;
        }

        int id = Integer.parseInt(tblBook.getValueAt(baris, 0).toString());
        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin hapus buku ini?", "Hapus Data", javax.swing.JOptionPane.YES_NO_OPTION);

        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            dao.BookDAO dao = new dao.BookDAO();
            if (dao.deleteBook(id)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Buku berhasil dihapus!");
                loadData();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

