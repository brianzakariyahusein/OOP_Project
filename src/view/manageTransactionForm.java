    private void loadData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblTransaction.getModel();
        model.setRowCount(0);

        dao.TransactionDAO transactionDAO = new dao.TransactionDAO();
        java.util.List<model.TransactionModel> list = transactionDAO.getAllTransactions();

        for (model.TransactionModel trans : list) {
            model.addRow(new Object[]{
                trans.getTransactionId(),
                trans.getMemberId(),
                trans.getBookId(),
                trans.getProcessedBy(),
                trans.getBorrowDate(),
                trans.getReturnDate(),
                trans.getStatus()
            });
        }
    }

