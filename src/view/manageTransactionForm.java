    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        int baris = tblTransaction.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih dulu data transaksi di tabel yang mau dikembalikan!");
            return;
        }

        int transactionId = Integer.parseInt(tblTransaction.getValueAt(baris, 0).toString());
        String status = tblTransaction.getValueAt(baris, 6).toString();

        if (status.equals("returned")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Buku ini sudah dikembalikan sebelumnya.");
            return;
        }

        java.sql.Date returnDate = new java.sql.Date(System.currentTimeMillis());
        dao.TransactionDAO transactionDAO = new dao.TransactionDAO();

        if (transactionDAO.returnBook(transactionId, returnDate)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Buku berhasil dikembalikan!");
            loadData();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memproses pengembalian.");
        }
    }//GEN-LAST:event_btnReturnActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        if (userRole.equals("admin")) {
            view.AdminDashboard adminDash = new view.AdminDashboard(loggedInUserId);
            adminDash.setVisible(true);
        } else if (userRole.equals("staff")) {
            view.StaffDashboard staffDash = new view.StaffDashboard(loggedInUserId);
            staffDash.setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnBorrowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrowActionPerformed
        int memberId = Integer.parseInt(txtMemberId.getText());
        int bookId = Integer.parseInt(txtBookId.getText());
        java.sql.Date borrowDate = new java.sql.Date(System.currentTimeMillis());

        model.TransactionModel trans = new model.TransactionModel(memberId, bookId, loggedInUserId, borrowDate, "borrowed");
        dao.TransactionDAO transactionDAO = new dao.TransactionDAO();

        if (transactionDAO.borrowBook(trans)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Buku berhasil dipinjam!");
            loadData();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memproses peminjaman.");
        }
    }//GEN-LAST:event_btnBorrowActionPerformed

