    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int baris = tblMember.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih dulu data di tabel yang mau diedit!");
            return;
        }

        int memberId = Integer.parseInt(tblMember.getValueAt(baris, 0).toString());
        String fullName = txtFullName.getText();
        String address = txtAddress.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();

        model.MemberModel member = new model.MemberModel(memberId, fullName, address, phone, email, loggedInUserId);
        dao.MemberDAO memberDAO = new dao.MemberDAO();

        if (memberDAO.updateMember(member)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
            loadData();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memperbarui data.");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String fullName = txtFullName.getText();
        String address = txtAddress.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();

        model.MemberModel member = new model.MemberModel(fullName, address, phone, email, loggedInUserId);
        dao.MemberDAO memberDAO = new dao.MemberDAO();

        if (memberDAO.insertMember(member)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data member berhasil ditambah!");
            loadData();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambah data.");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int baris = tblMember.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih dulu data yang mau dihapus!");
            return;
        }

        int memberId = Integer.parseInt(tblMember.getValueAt(baris, 0).toString());
        dao.MemberDAO memberDAO = new dao.MemberDAO();

        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin mau hapus member ini?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            if (memberDAO.deleteMember(memberId)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                loadData();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMemberMouseClicked
        int baris = tblMember.getSelectedRow();
        if (baris != -1) {
            txtFullName.setText(tblMember.getValueAt(baris, 1).toString());
            txtAddress.setText(tblMember.getValueAt(baris, 2).toString());
            txtPhone.setText(tblMember.getValueAt(baris, 3).toString());
            txtEmail.setText(tblMember.getValueAt(baris, 4).toString());
        }
    }//GEN-LAST:event_tblMemberMouseClicked

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        if (userRole.equals("admin")) {
            view.AdminDashboard adminDash = new view.AdminDashboard(loggedInUserId);
            adminDash.setVisible(true);
        } else if (userRole.equals("staff")) {
            view.StaffDashboard staffDash = new view.StaffDashboard(loggedInUserId);
            staffDash.setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnBackActionPerformed

