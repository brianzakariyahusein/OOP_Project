    private void loadData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblMember.getModel();
        model.setRowCount(0);

        dao.MemberDAO memberDAO = new dao.MemberDAO();
        java.util.List<model.MemberModel> list = memberDAO.getAllMembers();

        for (model.MemberModel member : list) {
            model.addRow(new Object[]{
                member.getMemberId(),
                member.getFullName(),
                member.getAddress(),
                member.getPhone(),
                member.getEmail(),
                member.getCreatedBy()
            });
        }
    }

